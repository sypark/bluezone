package com.bluezone.bil.domain.cust;

public class AnalysisCustRecord {

	private Integer custNo;
	
	private String nickName;
	
	private Integer score;
	
	private Integer gameCnt;
	
	private Float gameAvg;
	
	private Integer sumEarnScore;
	
	private Integer maxHighRun;
	
	private Integer sumWinCnt;
	
	private Integer sumLoseCnt;
	
	private Integer sumDrawCnt;
	
	public Integer getCustNo() {
		return custNo;
	}

	public void setCustNo(Integer custNo) {
		this.custNo = custNo;
	}

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public Integer getScore() {
		return score;
	}

	public void setScore(Integer score) {
		this.score = score;
	}

	public Integer getGameCnt() {
		return gameCnt;
	}

	public void setGameCnt(Integer gameCnt) {
		this.gameCnt = gameCnt;
	}

	public Float getGameAvg() {
		return gameAvg;
	}

	public void setGameAvg(Float gameAvg) {
		this.gameAvg = gameAvg;
	}

	public Integer getSumEarnScore() {
		return sumEarnScore;
	}

	public void setSumEarnScore(Integer sumEarnScore) {
		this.sumEarnScore = sumEarnScore;
	}

	public Integer getMaxHighRun() {
		return maxHighRun;
	}

	public void setMaxHighRun(Integer maxHighRun) {
		this.maxHighRun = maxHighRun;
	}

	public Integer getSumWinCnt() {
		return sumWinCnt;
	}

	public void setSumWinCnt(Integer sumWinCnt) {
		this.sumWinCnt = sumWinCnt;
	}

	public Integer getSumLoseCnt() {
		return sumLoseCnt;
	}

	public void setSumLoseCnt(Integer sumLoseCnt) {
		this.sumLoseCnt = sumLoseCnt;
	}

	public Integer getSumDrawCnt() {
		return sumDrawCnt;
	}

	public void setSumDrawCnt(Integer sumDrawCnt) {
		this.sumDrawCnt = sumDrawCnt;
	}
	
	
}
